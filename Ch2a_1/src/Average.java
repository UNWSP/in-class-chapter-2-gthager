
public class Average {
	public static void main(String args[]) {
		//declares heights
		double stu1 = 60;
		double stu2 = 63;
		double stu3 = 62.5;
		double stu4 = 78;
		double stu5 = 72;
		//finds total
		double total = stu1+stu2+stu3+stu4+stu5;
		//finds average
		double avg = total/5;
		//displays average
		System.out.println("The average height is "+avg+" inches.");
		
	}
}
